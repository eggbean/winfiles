# Makes Explorer window titlebars and borders thinner using registry values

$regPath = "HKCU:\Control Panel\Desktop\WindowMetrics"

if (-not (Test-Path $regPath)) {
    New-Item -Path $regPath -Force
}

Set-ItemProperty -Path $regPath -Name "CaptionHeight" -Value "-285" -Type String
Set-ItemProperty -Path $regPath -Name "CaptionWidth" -Value "-285" -Type String
Set-ItemProperty -Path $regPath -Name "CaptionFont" -Value ([byte[]] (0xf4,0xff,0xff,0xff,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x90,0x01,0x00,0x00,0x00,0x00,0x00,0x01,0x00,0x00,0x05,0x00,0x53,0x00,0x65,0x00,0x67,0x00,0x6f,0x00,0x65,0x00,0x20,0x00,0x55,0x00,0x49,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00)) -Type Binary
Set-ItemProperty -Path $regPath -Name "ScrollWidth" -Value "-240" -Type String
Set-ItemProperty -Path $regPath -Name "ScrollHeight" -Value "-240" -Type String
Set-ItemProperty -Path $regPath -Name "PaddedBorderWidth" -Value "0" -Type String
